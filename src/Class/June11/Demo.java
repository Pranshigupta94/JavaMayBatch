package Class.June11;

public class Demo {
    public static void main(String[] args) {

        /*
       two ways to create String :
       1. Literal -> String name = "Pranshi";
       2. creating String object using new -> String name = new String("Pranshi");
         */


        String name = "Pranshi";// using literal
        System.out.println(name);

        String lastName = new String("Gupta"); // using new keyword
        System.out.println(lastName);
    }
}